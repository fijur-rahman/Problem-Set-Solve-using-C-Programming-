#include <stdio.h>

int main()
{
    int a;
    float b;
    char x;
    scanf("%d", &a);
    scanf("%f", &b);
    getchar();
    scanf("%c", &x);
    printf("The Integer Value: %d\n", a);
    printf("The Floating point Value: %f\n", b);
    printf("The Character Value: %c\n", x);
    return 0;
}
